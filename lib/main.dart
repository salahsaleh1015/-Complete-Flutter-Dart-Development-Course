void main() {
  // switch case
 // work like (==)operator

  String grade = 'B';

  switch (grade) {
    case 'A':
      print("Excellent");
      break;
    case 'B':
      print("Good");
      break;
    case 'C':
      print("Fair");
      break;
    case 'D':
      print("Poor");
      break;
    default:
      print("Invalid");
  }
}
